export const translateWord = (word, targetLanguage = 'en') => {
  if (!word) return '';
  
  // Clean word (remove punctuation for lookup)
  const cleanWord = word.toLowerCase().replace(/[.,/#!$%^&*;:{}=\-_`~()?"']/g, "");
  
  const entry = dictionary[cleanWord];
  
  if (entry && entry[targetLanguage]) {
    return entry[targetLanguage];
  }
  
  // Fallback for unknown words
  return targetLanguage === 'en' ? 'Translation not found' : 'Traducción no encontrada';
};
